- (BOOL)application:(UIApplication *)application didFinishLaunchingWithOptions:(NSDictionary *)launchOptions {
    self.window = [[UIWindow alloc]initWithFrame:[UIScreen mainScreen].bounds];
   self.window.backgroundColor = [UIColor whiteColor];
    MenuTableViewController *mv = [[MenuTableViewController alloc]init];
    self.bvc = [[BaseTabBarController alloc]init];
    self.drawer = [[ICSDrawerController alloc]initWithLeftViewController:mv centerViewController:self.bvc];
    [self.window setRootViewController:self.drawer];
    [self.window makeKeyAndVisible];
    UIImage *bgImage = [UIImage imageNamed:@"bg2.jpg"];
    bgImage = [self OriginImage:bgImage scaleToSize:mv.view.size];
    self.window.backgroundColor = [UIColor colorWithPatternImage:bgImage];
    return YES;
}

-(UIImage*)OriginImage:(UIImage *)image scaleToSize:(CGSize)size
{
    UIGraphicsBeginImageContext(size);  //size 为CGSize类型，即你所需要的图片尺寸
    [image drawInRect:CGRectMake(-30, 0, size.width, size.height)];
    
    UIImage* scaledImage = UIGraphicsGetImageFromCurrentImageContext();
    
    UIGraphicsEndImageContext();
    
    return scaledImage;   //返回的就是已经改变的图片
}

